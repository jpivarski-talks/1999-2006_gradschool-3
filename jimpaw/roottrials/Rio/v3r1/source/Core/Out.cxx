//////////////////////////////////////////////////////////////////////////////
Rio::Out& Rio::Out::operator<<(
 const char* aValue
) 
//////////////////////////////////////////////////////////////////////////////
//!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!//
{
  fPrinter.write(aValue);
  return *this;
}
